    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int monto = (Integer) jSpinnerMonto.getValue();
        float interes = (Integer) jSpinnerInteres.getValue();
        interes = interes / 100;
        int meses = (Integer) jSpinnerPlazo.getValue();
        int sel = jComboBoxClientes.getSelectedIndex();
        Prestamo p = new Prestamo(monto, meses, interes);
        this.p.clientes.get(sel).addPrestamo(p);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

