    private void init() throws Exception {
        this.jComboBoxClientes.removeAllItems();
        if (p.clientes.isEmpty()) {
            JOptionPane.showConfirmDialog(this, "Agrege algun cliente primero.");
            throw new Exception("Agrege algun cliente primero.");
        } else {
            for (Cliente c : p.clientes) {
                jComboBoxClientes.addItem(c.toString());
            }
        }
    }

