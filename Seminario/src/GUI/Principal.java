    public void show(Prestamo p) {
        DefaultTableModel modelo = (DefaultTableModel) jTablaAmortizacion.getModel();
        int a = modelo.getRowCount();
        for (int i = 0; i < a; i++) {
            modelo.removeRow(0);
        }
        float cuota = (p.monto + (p.monto * p.intereses)) / p.meses;
        float interes = (p.monto * p.intereses) / p.meses;
        float capital = (p.monto / p.meses);
        float total = (p.monto + (p.monto * p.intereses));
        Object[] rowData = new Object[5];
        for (int i = 1; i <= p.meses; i++) {
            rowData[0] = i;
            rowData[1] = cuota;
            rowData[2] = interes;
            rowData[3] = capital;
            rowData[4] = total - (cuota * i);

            modelo.addRow(rowData);
        }
        jTablaAmortizacion.setModel(modelo);

    }

